export function getStateByAbbreviation(state: StateAbbreviation): State {
  return states.find(({ abbreviation }) => state === abbreviation)!
}

export const stateNames = states.map(state => state.name)
export type StateName = typeof stateNames[number]

export function isStateName(value: unknown): value is StateName {
  return stateNames.includes(value as StateName)
}

export const stateAbbreviations = states.map(state => state.abbreviation)
export type StateAbbreviation = typeof stateAbbreviations[number]

export function isStateAbbreviation(value: unknown): value is StateAbbreviation {
  return stateAbbreviations.includes(value as StateAbbreviation)
}